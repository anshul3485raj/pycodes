# python program to generate emoji

# We can use emoji_name as well as UNICODE too..


# importing emoji module
import emoji

# smiling face with smiling eyes
print(emoji.emojize(':smiling_face_with_smiling_eyes:'))

# face with tears of joy
print(emoji.emojize(':face_with_tears_of_joy:'))

# star-struck
print(emoji.emojize(':star-struck:'))

# hugging face
print(emoji.emojize(':hugging_face:'))

# kissing face
print(emoji.emojize(':kissing_face:'))

# neutral face
print(emoji.emojize(':neutral_face:'))

# lying face
print(emoji.emojize(":lying_face:"))

